export function toPinyinName(input: string): string {
  const parts: string[] = [];
  for (const char of input.trim()) {
    if (/^[a-z0-9]$/i.test(char)) {
      parts.push(char.toLowerCase());
      continue;
    }
    const mapped = PINYIN_MAP[char];
    if (mapped) parts.push(mapped);
  }
  return parts.join("");
}
